QUnit.test( "Déclinaison française", function( assert ) {
    Object.assign(globalThis,jsRealB);
    loadFr();
    var exp=N("acheteur").g("f").n("p")
    assert.equal(exp.realize(), "acheteuses", exp.toSource());

    for (let [word,wordInfo] of Object.entries(testsNomAdjDeclinaisonFr)){
        for (let [pos,posInfo] of Object.entries(wordInfo)){
            var exp0;
            if (pos=="N"){
                exp0=N(word)
            } else if (pos=="A"){
                exp0=A(word)
            } else {
                window.alert("POS inconnu:word")
            }
            for (let [flags,expected] of Object.entries(posInfo)){
                exp=exp0.clone();
                switch (flags) {
                    case "m":exp=exp.g("m");break;
                    case "ms":exp=exp.g("m").n("s");break;
                    case "mp":exp=exp.g("m").n("p");break;
                    case "f":exp=exp.g("f");break;
                    case "fs":exp=exp.g("f").n("s");break;
                    case "fp":exp=exp.g("f").n("p");break;
                    case "s":exp=exp.n("s");break;
                    case "p":exp=exp.n("p");break;
                    case "xp":exp=exp.n("p");break;
                    case "co":exp=exp.f("co");break;
                default:
                    alert("flags inconnu:"+flags+":"+word)
                }
                var res=exp.realize()
                assert.equal(res,expected,exp.toSource()+"=>"+res)
            }
        }
    }
    
    for (let [word,wordInfo] of Object.entries(testsProDeclinaisonFr)){
        for (let [flags,expected] of Object.entries(wordInfo)){
            exp=Pro(word).g(flags.charAt(0)).n(flags.charAt(1)).pe(+flags.charAt(2))
            var res=exp.realize()
            assert.equal(res,expected,exp.toSource()+"=>"+res)
        }
    }
})
